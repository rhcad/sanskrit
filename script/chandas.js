const METRES = window.METRES = {}
const METRES0 = window.METRES0 = {}
const YATIS = window.YATIS = {}

/**
 * 规则韵律(samvṛtta)，四个音步的音节模式相同.
 * 两个诗行(the Line of Verse，短竖线标识)分别含两个音步(pāda，格律单位)
 * @param {string} name 韵律名称
 * @param {string} metre 一个音步的音节模式(syllable pattern)，每个字符为 L、G 或 x
 * @param {number} [yati] 一个诗行在哪个音节处划分音步，即奇数音步的音节数
 * @param {number} [yati2] 一个诗行分为三个音步时指定第二音步的音节数
 */
function AddSamavrtta(name, metre, yati=0, yati2=0) {
  if (metre.indexOf(' ') >= 0) {
    METRES0[name] = [metre, metre]
  }
  metre = metre.replace(/\s/g, '')
  console.assert(!METRES[name], name)
  METRES[name] = metre
  if (yati) {
    YATIS[name] = yati2 ? [yati, yati2] : [yati]
    YATIS[name].push(metre.length - yati - yati2)
  }
}

/**
 * 半规则韵律(ardhasamvṛtta)，奇偶音步各有其音节模式
 * @param {string} name 韵律名称
 * @param {string} metre1 奇数音步的音节模式，每个字符为 L、G 或 x
 * @param {string} metre2 偶数音步的音节模式，每个字符为 L、G 或 x
 */
function AddArdhasamavrtta(name, metre1, metre2) {
  AddVishamavrtta(name, metre1, metre2, '', '')
  if ((metre1 + metre2).indexOf(' ') >= 0) {
    METRES0[name] = [metre1, metre2]
  }
}

/**
 * 不规则韵律，每个音步各有其音节模式
 * @param {string} name 韵律名称
 * @param {string} metre1 第1音步的音节模式，每个字符为 L、G 或 x
 * @param {string} metre2 第2音步的音节模式
 * @param {string} metre3 第3音步的音节模式
 * @param {string} metre4 第4音步的音节模式
 */
function AddVishamavrtta(name, metre1, metre2, metre3, metre4) {
  console.assert(!METRES[name], name)
  metre1 = metre1.replace(/\s/g, '')
  metre2 = metre2.replace(/\s/g, '')
  METRES[name] = metre3 ? [metre1, metre2, metre3.replace(/\s/g, ''), metre4.replace(/\s/g, '')] : [metre1, metre2]
}

