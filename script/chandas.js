/**
 * 给定颂文的一个诗行，计算其韵律(每个音节的轻重)
 * @param {string} text 诗行的IAST文本
 * @param {number} index 音步序号
 * @returns {string} 韵律，由'L'(轻音节)和'G'(重音节)组成
 */
const calcMetre = window.calcMetre = function (text, index) {
  let syllables = Sanscript.splitAksara(text.replace(/▷\d+[a-f]?/g, ''))
  let types = syllables.map(s => Sanscript.getAksaraType(s) + '')

  // 只保留常规音节
  syllables = syllables.filter((s, i) => /[0-6]/.test(types[i]))
  types = types.filter((s, i) => /[0-6]/.test(types[i]))
  const lights = []

  for (let i = 0; i < syllables.length - 1; ++i) {
    // 当前音节如果有元音
    if ('456'.indexOf(types[i]) >= 0) {
      // 双元音，或含随韵、止音，则为重音节
      const vowels = syllables[i].replace(/[^aiuāīūṛṝḷḹáíúeēèoōòṃḥ]/gi, '')
      if (vowels.length > 1 || /[āīūṝḹeēèoōò]/i.test(vowels)) {
        lights[i] = 'G'
      }
      // 单元音后有复辅音，则为重音节
      else if (types[i + 1] === '2' || types[i + 1] === '6') {
        lights[i] = 'G'
      } else { // 其余单元音为轻音节
        lights[i] = 'L'
      }
    } else { // 纯辅音：合并到后面的音节
      syllables[i + 1] = syllables[i] + syllables[i + 1]
      types[i + 1] = Sanscript.getAksaraType(syllables[i + 1])
      syllables.splice(i, 1)
      types.splice(i, 1)
      i -= 2
    }
  }

  // 末音节为纯辅音、倒数第二个音节有元音，则合并为一个音节
  if (syllables.length > 1 && '456'.indexOf(types[types.length - 1]) < 0) {
    syllables[syllables.length - 2] += syllables[syllables.length - 1]
    syllables.splice(syllables.length - 1, 1)
    lights.splice(lights.length - 1, 1)
  }
  if (syllables.length > 1) {
    lights.push('G') // 末音节为重音节
  }

  return lights.join('')
}

const METRES_KV = Object.entries(METRES)
const getMetreTemplate = (i) => METRES_KV[i][1]

/**
 * 匹配一个音步的韵律
 * @param {string} metre 一个音步的韵律，每个字符为L或G
 * @param {string} template 韵律模版，每个字符为 L、G 或 x
 * @returns {boolean}
 */
const matchMetre = (metre, template) => {
  if (template.length !== metre.length) {
    return false
  }
  if (template.indexOf('x') < 0) {
    return template === metre
  }
  let n = template.length
  while (--n >= 0 && (template[n] === 'x' || template[n] === metre[n])) {}
  return n < 0
}

/**
 * 根据一个音步的韵律，查找规则韵律的名称
 * @param {string} metre 一个音步的韵律，每个字符为L或G
 * @returns {string} 韵律名称
 */
const findSamNameByMetre = (metre) => {
  for (let i = 0; i < METRES_KV.length; i++) {
    if (matchMetre(metre, getMetreTemplate(i))) {
      return METRES_KV[i][0]
    }
  }
}

/**
 * 给定各音步的韵律，查找半规则韵律或不规则韵律的名称
 * @param {string[]} metres 各音步的韵律，每个字符为L或G
 * @returns {string} 韵律名称
 */
const findNameByMetres = (metres) => {
  for (let i = 0; i < METRES_KV.length; i++) {
    if (Array.isArray(getMetreTemplate(i))) {
      const m = METRES_KV[i], template = m[1], n = template.length
      if (n === 2) {
        if (matchMetre(metres[0], template[0])
          && matchMetre(metres[1], template[1])) {
          return m[0]
        }
      } else if (n === 4 && metres.length >= 4) {
        const r = template.filter((t, i) => matchMetre(metres[i], t))
        if (r.length > 2) {
          return m[0]
        }
      }
    }
  }
}

const _checkUpajati = (name1, name2, m1, m2) => {
  if (m1 === m2) {
    return m1 && [name1, '']
  }
  if (name1 && name2 && name1 !== name2) {
    if ((name1.indexOf('indravajrā') >= 0 || name1.indexOf('upendravajrā') >= 0)
        && (name2.indexOf('indravajrā') >= 0 || name2.indexOf('upendravajrā') >= 0)) {
      return ['7-upajāti', '']
    }
    if ((name1.indexOf('indravaṃśa') >= 0 || name1.indexOf('vaṃśastha') >= 0)
        && (name2.indexOf('indravaṃśa') >= 0 || name2.indexOf('vaṃśastha') >= 0)) {
      return ['12-upajāti', '']
    }
  }
  const ardha = m1 && findNameByMetres([m1, m2])
  if (ardha) {
    return [ardha, m1.length === m2.length ? '' : '半规则']
  }
}

const hzPuncG = /[\u4e00-\u9fa5\uFF01-\uFF5E\u3000-\u303F()]+/g

/**
 * 给定一个诗节文本，查找其韵律
 * @param {string} text 诗节IAST文本
 * @returns {[]|string[]} [name, type, m1, m2, m3, m4]
 */
window.verseMetre = function(text) {
  // 去除音频标记
  const t1 = (text || '').replace(/(➁\s*)?▷\d+[a-f]?/g, '')
  // 去除行末的 |、||、|number| ，去除连字符、(...)
  const t2 = t1.replace(/\|+(\d[\d.-]*\|+)?\s*$|-|\([^)]+\)/g, '')
    .replace(hzPuncG, '')
  // 拆分为每音步一行，行内有诗行标点就按标点拆行
  const lines = t2.trim().split(/\s*[|\n]+\s*/).filter(s => s)
  if (lines.length < 2) {
    return []
  }

  const metres = lines.map(calcMetre)
  let m1 = metres[0], m2 = metres[1]
  let m3 = metres[2], m4 = metres[3]

  if (lines.length === 2 && (m1.length % 2 === 0 || m2.length % 2 === 0
    || m1.length === m2.length && m1.length > 20)) {
    const n = parseInt((m1.length % 2 === 0 ? m1.length / 2 : m2.length / 2) + '')
    if (Math.abs(m1.length - m2.length) < 2) {
      m4 = m2.substr(n)
      m3 = m2.substr(0, n)
      m2 = m1.substr(n)
      m1 = m1.substr(0, n)
    }
  }
  const ms = m3 && m4 ? [m1, m2, m3, m4] : [m1, m2]
  let name1 = findSamNameByMetre(m1)
  let name2 = findSamNameByMetre(m2)
  let type = ''
  if (!name1 && m1.endsWith('L')) {
    const m_ = m1.substring(0, m1.length - 1) + 'G'
    name1 = findSamNameByMetre(m_)
    if (name1) {
      m1 = m_
    }
  }

  let r = _checkUpajati(name1, name2, m1, m2)
  if (r) {
    return r.concat(ms)
  }
  r = m3 && m4 && _checkUpajati(findSamNameByMetre(m3), findSamNameByMetre(m4), m3, m4)
  if (r) {
    return r.concat(ms)
  }
  if (!name1) {
    name1 = findNameByMetres([m1, m2])
    if (name1) {
      type = m1.length === m2.length ? '' : '半规则'
    } else if (m3) {
      name1 = findNameByMetres(ms);
      if (name1) {
        type = '不规则'
      } else {
        type = m1 === m3 && m2 === m4 ? '半规则' : '不规则'
        return [name1, type].concat(ms)
      }
    }
  }

  return [name1, type].concat(ms)
}

function _yatiMetre(metre, yatis) {
  metre = Array.isArray(metre) ? metre[0] : metre
  metre = metre.replace(/G/g, '长').replace(/L/g, '短')
  let metre2
  if (yatis) {
    const metres = [], m2 = metre.split('')
    yatis.map(y => m2 && metres.push(m2.splice(0, y).join('')))
    metre2 = metres.join(' + ')
    yatis = ' ' + yatis.map(y => y + '').join('+')
  } else {
    const metres = [], m2 = metre.split('')
    for (let i = 0; m2.length; i++) {
      metres.push(m2.splice(0, 3).join(''))
    }
    metre2 = metres.join(' ')
  }
  return {metre, metre2, yatis}
}

/**
 * 得到韵律描述
 * @param {string} name 韵律名称
 * @param {string} m1 第一音步的音节模式
 * @param {string} m2 第二音步的音节模式
 * @returns {string}
 */
window.getYati = function (name, m1, m2) {
  let yatis = YATIS[name] || ''
  let metre = METRES[name] || m1 || ''
  const r = _yatiMetre(metre, yatis)
  if (m2 && m2.length !== r.metre.length) {
    return `（${r.metre.length}/${m2.length}音节）`
  }
  return `（${r.metre.length}音节${r.yatis}）`
}

